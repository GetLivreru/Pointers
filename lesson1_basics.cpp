#include <iostream>
using namespace std;

/**
 * УРОК 1: ОСНОВЫ УКАЗАТЕЛЕЙ
 * 
 * Указатель - это переменная, которая хранит АДРЕС другой переменной
 * 
 * Основные операторы:
 * & - оператор взятия адреса
 * * - оператор разыменования (получения значения по адресу)
 */

int main() {
    cout << "=== УРОК 1: Основы указателей ===" << endl;
    
    // 1. Обычная переменная
    int number = 42;
    cout << "Переменная number = " << number << endl;
    cout << "Адрес переменной number = " << &number << endl;
    
    // 2. Создаем указатель
    int* ptr = &number;  // ptr теперь "указывает" на number
    
    cout << "\nУказатель ptr:" << endl;
    cout << "Значение ptr (адрес) = " << ptr << endl;
    cout << "Значение по адресу (*ptr) = " << *ptr << endl;
    
    // 3. Изменяем значение через указатель
    *ptr = 100;
    cout << "\nПосле *ptr = 100:" << endl;
    cout << "number = " << number << endl;  // Изменилось!
    cout << "*ptr = " << *ptr << endl;
    
    // 4. Аналогия с домом
    cout << "\n=== АНАЛОГИЯ ===" << endl;
    cout << "Представь: number - это вещь в комнате №" << &number << endl;
    cout << "ptr - это записка с номером этой комнаты" << endl;
    cout << "Когда мы идем по адресу (*ptr), мы находим вещь: " << *ptr << endl;
    
    return 0;
}
